#include "struct.h"

int	some_fish_left(t_map *map)
{
	int	i;

	while (map)
	{
		i = 0;
		while ((map->line)[i])
		{
			if ((map->line)[i] == 'C')
				return (1);
			i++;
		}
		map = map->next;
	}
	return (0);
}
